// Filter functions for portfolio display
export function getFeaturedProjects() {
  return portfolioProjects.filter(project => project.featured)
}

export function getProjectsByIndustry(industry) {
  return portfolioProjects.filter(project => project.industry === industry)
}

export function getProjectBySlug(slug) {
  return portfolioProjects.find(project => project.slug === slug)
}

export function getAllIndustries() {
  return [...new Set(portfolioProjects.map(project => project.industry))]
}

export function getRealProjects() {
  return portfolioProjects.filter(project => project.url !== null)
}

export function getExampleProjects() {
  return portfolioProjects.filter(project => project.url === null)
}

// Generate schema for individual project
export function generateProjectSchema(project) {
  const schema = {
    '@context': 'https://schema.org',
    '@type': 'CreativeWork',
    name: project.title,
    description: project.description,
    creator: {
      '@type': 'Organization',
      name: 'EaseBuilds',
      url: 'https://easebuilds.in',
      telephone: '+91-6283380110'
    },
    datePublished: project.completedDate,
    keywords: `${project.category}, ${project.industry}, web development patiala, ${project.businessName}`,
    about: {
      '@type': 'LocalBusiness',
      name: project.businessName,
      address: {
        '@type': 'PostalAddress',
        addressLocality: 'Patiala',
        addressRegion: 'Punjab',
        addressCountry: 'IN'
      }
    }
  }

  // Add URL if project is live
  if (project.url) {
    schema.url = project.url
    schema.workExample = {
      '@type': 'WebSite',
      url: project.url
    }
  }

  return schema
}

